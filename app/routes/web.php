<?php

use Illuminate\Support\Facades\Route;

/*
|--------------------------------------------------------------------------
| Web Routes
|--------------------------------------------------------------------------
| ここでアプリケーションのWebルートを登録します。
*/

// ===================================
// 1. 認証 (Auth) 関連のルート
// ===================================
Auth::routes(); 


// ===================================
// 2. ホーム画面 (ServiceController@index)
// ===================================
// トップページとホーム画面は、ServiceControllerのindexメソッドを呼び出す
Route::get('/', 'ServiceController@index')->name('home');


// ===================================
// 3. 投稿 (Service) 関連のルート (認証不要)
// ===================================
// 詳細表示(show)のみ、ログインなしでアクセス可能
Route::resource('services', 'ServiceController')->only(['show']);


